-- 10. MedicalOrders (Base)
CREATE TABLE "MedicalOrders" (
    "Id" bigint GENERATED BY DEFAULT AS IDENTITY,
    "PatientId" text NOT NULL,
    "DoctorId" text NOT NULL,
    "NurseId" text,
    "PlantEndTime" timestamp with time zone NOT NULL,
    "EndTime" timestamp with time zone,
    "OrderType" text NOT NULL,
    "Status" text NOT NULL,
    "IsLongTerm" boolean NOT NULL,
    "CreateTime" timestamp with time zone NOT NULL,
    CONSTRAINT "PK_MedicalOrders" PRIMARY KEY ("Id"),
    CONSTRAINT "FK_MedicalOrders_Patients_PatientId" FOREIGN KEY ("PatientId") REFERENCES "Patients" ("Id") ON DELETE CASCADE,
    CONSTRAINT "FK_MedicalOrders_Doctors_DoctorId" FOREIGN KEY ("DoctorId") REFERENCES "Doctors" ("Id") ON DELETE CASCADE,
    CONSTRAINT "FK_MedicalOrders_Nurses_NurseId" FOREIGN KEY ("NurseId") REFERENCES "Nurses" ("Id")
);

