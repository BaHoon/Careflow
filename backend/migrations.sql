-- 1. Departments
CREATE TABLE "Departments" (
    "Id" text NOT NULL,
    "DeptName" text NOT NULL,
    "Location" text NOT NULL,
    "CreateTime" timestamp with time zone NOT NULL,
    CONSTRAINT "PK_Departments" PRIMARY KEY ("Id")
);

-- 2. Staffs (Base)
CREATE TABLE "Staffs" (
    "Id" text NOT NULL,
    "EmployeeNumber" text NOT NULL,
    "PasswordHash" text NOT NULL,
    "Name" text NOT NULL,
    "IdCard" text NOT NULL,
    "Phone" text NOT NULL,
    "RoleType" text NOT NULL,
    "IsActive" boolean NOT NULL,
    "DeptCode" text NOT NULL,
    "CreateTime" timestamp with time zone NOT NULL,
    CONSTRAINT "PK_Staffs" PRIMARY KEY ("Id"),
    CONSTRAINT "FK_Staffs_Departments_DeptCode" FOREIGN KEY ("DeptCode") REFERENCES "Departments" ("Id") ON DELETE CASCADE
);

-- 3. Doctors (Derived)
CREATE TABLE "Doctors" (
    "Id" text NOT NULL,
    "Title" text NOT NULL,
    "PrescriptionAuthLevel" text NOT NULL,
    CONSTRAINT "PK_Doctors" PRIMARY KEY ("Id"),
    CONSTRAINT "FK_Doctors_Staffs_Id" FOREIGN KEY ("Id") REFERENCES "Staffs" ("Id") ON DELETE CASCADE
);

-- 4. Nurses (Derived)
CREATE TABLE "Nurses" (
    "Id" text NOT NULL,
    "NurseRank" text NOT NULL,
    CONSTRAINT "PK_Nurses" PRIMARY KEY ("Id"),
    CONSTRAINT "FK_Nurses_Staffs_Id" FOREIGN KEY ("Id") REFERENCES "Staffs" ("Id") ON DELETE CASCADE
);

-- 5. Wards
CREATE TABLE "Wards" (
    "Id" text NOT NULL,
    "DepartmentId" text NOT NULL,
    "CreateTime" timestamp with time zone NOT NULL,
    CONSTRAINT "PK_Wards" PRIMARY KEY ("Id"),
    CONSTRAINT "FK_Wards_Departments_DepartmentId" FOREIGN KEY ("DepartmentId") REFERENCES "Departments" ("Id") ON DELETE CASCADE
);

-- 6. Beds
CREATE TABLE "Beds" (
    "Id" text NOT NULL,
    "WardId" text NOT NULL,
    "Status" text NOT NULL,
    "CreateTime" timestamp with time zone NOT NULL,
    CONSTRAINT "PK_Beds" PRIMARY KEY ("Id"),
    CONSTRAINT "FK_Beds_Wards_WardId" FOREIGN KEY ("WardId") REFERENCES "Wards" ("Id") ON DELETE CASCADE
);

-- 7. Patients
CREATE TABLE "Patients" (
    "Id" text NOT NULL,
    "Name" text NOT NULL,
    "Gender" text NOT NULL,
    "IdCard" text NOT NULL,
    "DateOfBirth" timestamp with time zone NOT NULL,
    "Age" integer NOT NULL,
    "Weight" real NOT NULL,
    "Status" text NOT NULL,
    "PhoneNumber" text NOT NULL,
    "NursingGrade" integer NOT NULL,
    "BedId" text NOT NULL,
    "AttendingDoctorId" text NOT NULL,
    "CreateTime" timestamp with time zone NOT NULL,
    CONSTRAINT "PK_Patients" PRIMARY KEY ("Id"),
    CONSTRAINT "FK_Patients_Beds_BedId" FOREIGN KEY ("BedId") REFERENCES "Beds" ("Id") ON DELETE CASCADE,
    CONSTRAINT "FK_Patients_Doctors_AttendingDoctorId" FOREIGN KEY ("AttendingDoctorId") REFERENCES "Doctors" ("Id") ON DELETE CASCADE
);

-- 8. Drugs
CREATE TABLE "Drugs" (
    "Id" character varying(50) NOT NULL,
    "GenericName" character varying(200) NOT NULL,
    "TradeName" character varying(200),
    "Manufacturer" character varying(200),
    "Specification" character varying(100) NOT NULL,
    "DosageForm" character varying(50) NOT NULL,
    "PackageSpec" character varying(100),
    "AtcCode" character varying(20),
    "Category" character varying(100),
    "AdministrationRoute" character varying(50) NOT NULL,
    "UnitPrice" numeric(10,4) NOT NULL,
    "PriceUnit" character varying(20) NOT NULL,
    "IsPrescriptionOnly" boolean NOT NULL,
    "IsNarcotic" boolean NOT NULL,
    "IsPsychotropic" boolean NOT NULL,
    "IsAntibiotic" boolean NOT NULL,
    "Status" character varying(20) NOT NULL,
    "Indications" character varying(1000),
    "Contraindications" character varying(1000),
    "DosageInstructions" character varying(500),
    "SideEffects" character varying(1000),
    "StorageConditions" character varying(200),
    "ShelfLifeMonths" integer,
    "ApprovalNumber" character varying(50),
    "Remarks" character varying(500),
    "IsDeleted" boolean NOT NULL,
    "DeleteTime" timestamp with time zone,
    "CreateTime" timestamp with time zone NOT NULL,
    CONSTRAINT "PK_Drugs" PRIMARY KEY ("Id")
);

